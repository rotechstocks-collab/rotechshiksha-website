export const LOCAL_VIDEOS_KEY = "rotech_custom_videos";

export function getLocalVideos(): VideoItem[] {
  if (typeof window === "undefined") return [];
  try {
    const saved = localStorage.getItem(LOCAL_VIDEOS_KEY);
    if (saved) {
      return JSON.parse(saved);
    }
  } catch (e) {}
  return [];
}

export function saveLocalVideo(video: VideoItem): void {
  if (typeof window === "undefined") return;
  try {
    const existing = getLocalVideos();
    existing.push(video);
    localStorage.setItem(LOCAL_VIDEOS_KEY, JSON.stringify(existing));
  } catch (e) {}
}

export function getAllVideos(): VideoItem[] {
  return [...videos, ...getLocalVideos()];
}

export function getPlaylistNames(): string[] {
  return Array.from(new Set(videos.map((v) => v.playlistName)));
}
